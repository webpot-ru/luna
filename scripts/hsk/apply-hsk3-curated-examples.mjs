function hasToneNumber(value) {
  return /(?:[A-Za-züÜv:]+[1-5]\b|\b[1-5]\s*$)/u.test(value);
}

const byKey = Object.fromEntries(
  examples.map(([key, example_zh, example_pinyin, example_en]) => [
    key,
    { example_zh, example_pinyin, example_en },
  ])
);

const duplicates = examples
  .map(([key]) => key)
  .filter((key, index, keys) => keys.indexOf(key) !== index);
if (duplicates.length) throw new Error(`Duplicate HSK3 example keys: ${duplicates.join(", ")}`);

for (const [key, value] of Object.entries(byKey)) {
  const sourceParts = key.replace(/^\d+:/u, "").split("…").filter(Boolean);
  if (!sourceParts.every((part) => value.example_zh.includes(part))) {
    throw new Error(`Example for ${key} does not contain required source parts: ${value.example_zh}`);
  }
  if (hasToneNumber(value.example_pinyin)) {
    throw new Error(`Tone-number pinyin in ${key}: ${value.example_pinyin}`);
  }
}

