const toneNumberPattern = /(?:[a-züv:]+[1-5]|[1-5][a-züv:]+)/iu;
const toneMarkPattern = /[āáǎàēéěèīíǐìōóǒòūúǔùǖǘǚǜĀÁǍÀĒÉĚÈĪÍǏÌŌÓǑÒŪÚǓÙǕǗǙǛńňǹḿ]/u;

function todayStamp() {
  return new Date().toISOString().slice(0, 10).replace(/-/gu, "");
}

function hasPlaceholder(value) {
  return /^我今天学习“.+”这个词。$/u.test(value.example_zh ?? "");
}

async function main() {
  const [sourceText, examplesText] = await Promise.all([
    fs.readFile(sourcePath, "utf8"),
    fs.readFile(examplesPath, "utf8"),
  ]);
  const sourceRows = JSON.parse(sourceText);
  const sourceKeys = new Set(sourceRows.map((row) => row.hsk_canonical_source?.hsk_key));
  const examples = JSON.parse(examplesText);
  const report = {
    release_id: releaseId,
    generated_at: new Date().toISOString(),
    applied: 0,
    total_rows: sourceRows.length,
    remaining_placeholders: 0,
    blockers: [],
  };

  for (const [key, tuple] of Object.entries(curatedExamples)) {
    if (!sourceKeys.has(key)) {
      report.blockers.push({ key, issue: "unknown_hsk_key" });
      continue;
    }
    const [example_zh, example_pinyin, example_en] = tuple;
    const word = key.slice(key.indexOf(":") + 1);
    if (!example_zh.includes(word)) report.blockers.push({ key, issue: "example_missing_source_word", example_zh });
    if (toneNumberPattern.test(example_pinyin)) report.blockers.push({ key, issue: "tone_number_pinyin", example_pinyin });
    if (!toneMarkPattern.test(example_pinyin)) report.blockers.push({ key, issue: "missing_tone_mark", example_pinyin });
    if (/[\u3400-\u9fff]/u.test(example_pinyin)) report.blockers.push({ key, issue: "han_in_pinyin", example_pinyin });
    examples[key] = { example_zh, example_pinyin, example_en };
    report.applied += 1;
  }

  report.remaining_placeholders = Object.values(examples).filter(hasPlaceholder).length;

  if (report.blockers.length) {
    console.error(JSON.stringify(report, null, 2));
    process.exitCode = 1;
    return;
  }

  await fs.mkdir(qaDir, { recursive: true });
  const reportPath = path.join(qaDir, `${releaseId}_curated_examples_batch1_${todayStamp()}.json`);
  await Promise.all([
    fs.writeFile(examplesPath, `${JSON.stringify(examples, null, 2)}\n`, "utf8"),
    fs.writeFile(reportPath, `${JSON.stringify(report, null, 2)}\n`, "utf8"),
  ]);

  console.log(
    JSON.stringify(
      {
        release_id: releaseId,
        applied: report.applied,
        remaining_placeholders: report.remaining_placeholders,
        examples: path.relative(ROOT, examplesPath),
        report: path.relative(ROOT, reportPath),
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
