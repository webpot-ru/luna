function applyVariant(baseDisplay, baseExample, meaningId, overrides) {
  const override = overrides[meaningId];
  if (!override) {
    return {
      display_headword: baseDisplay,
      example: baseExample,
      variant_status: "same_surface_as_canonical_source_layer",
      variant_note: "No spelling/example override needed for this row.",
    };
  }
  return override;
}

function buildLayerRow(rowReview, exampleRow, generatedAt) {
  if (rowReview.row_id !== exampleRow.row_id) {
    throw new Error(`Row id mismatch while building edition layer: ${rowReview.row_id} != ${exampleRow.row_id}`);
  }
  const baseDisplay = normalizeText(exampleRow.reviewed_display_headword);
  const baseExample = normalizeText(exampleRow.example_EN);
  const us = applyVariant(baseDisplay, baseExample, rowReview.meaning_id, US_OVERRIDES_BY_MEANING_ID);
  const gb = applyVariant(baseDisplay, baseExample, rowReview.meaning_id, GB_OVERRIDES_BY_MEANING_ID);
  const semanticScene = {
    ...(rowReview.semantic_scene ?? {}),
    edition_layer_rule_version: "oxford-us-uk-edition-layer-v1",
    edition_layer_status: "reviewed_draft_overlay",
  };
  return {
    release_id: rowReview.release_id,
    course_id: rowReview.course_id,
    row_id: rowReview.row_id,
    core_item_id: rowReview.core_item_id,
    meaning_id: rowReview.meaning_id,
    source_candidate_id: rowReview.source_candidate_id,
    source_language: "EN",
    canonical_source_variant: "Oxford 3000 source row with LunaCards-authored English layer",
    source_headword: rowReview.source_headword,
    reviewed_part_of_speech: rowReview.reviewed_part_of_speech,
    sense_no: rowReview.sense_no,
    core_band: rowReview.core_band,
    level_min: rowReview.level_min,
    level_max: rowReview.level_max,
    benchmark_membership: rowReview.benchmark_membership,
    source_status: "user_reported_oup_permission_pending_written_evidence",
    meaning_note: rowReview.meaning_note,
    semantic_scene: semanticScene,
    display_headword_EN_US: us.display_headword,
    example_EN_US: us.example,
    edition_status_EN_US: us.variant_status,
    edition_note_EN_US: us.variant_note,
    display_headword_EN_GB: gb.display_headword,
    example_EN_GB: gb.example,
    edition_status_EN_GB: gb.variant_status,
    edition_note_EN_GB: gb.variant_note,
    edition_layer_status: "draft_overlay_ready_for_pronunciation_and_export",
    reviewer: "codex_oxford_part_us_uk_edition_layer_v1",
    reviewed_at: generatedAt,
    generation_ready: false,
    remaining_blockers: exampleRow.remaining_blockers ?? [],
  };
}

