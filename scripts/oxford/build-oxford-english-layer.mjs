function parseArgs(argv) {
  const args = {
    contract: "config/oxford_3000_core_a1_part_002_300_v1_contract_v0.json",
    rowReview: "",
    outDirExamples: "outputs/oxford-vocabulary/examples",
    outDirEditionLayers: "outputs/oxford-vocabulary/edition-layers",
    examplesId: "english_examples_v1",
    layerId: "us_uk_edition_layer_v1",
    exampleMap: "",
  };
  for (let index = 0; index < argv.length; index += 1) {
    const key = argv[index];
    const value = argv[index + 1];
    if (!key.startsWith("--")) continue;
    if (value === undefined || value.startsWith("--")) {
      throw new Error(`Missing value for ${key}`);
    }
    index += 1;
    if (key === "--contract") args.contract = value;
    else if (key === "--row-review") args.rowReview = value;
    else if (key === "--examples-out-dir") args.outDirExamples = value;
    else if (key === "--edition-layer-out-dir") args.outDirEditionLayers = value;
    else if (key === "--examples-id") args.examplesId = value;
    else if (key === "--layer-id") args.layerId = value;
    else if (key === "--example-map") args.exampleMap = value;
    else throw new Error(`Unknown argument: ${key}`);
  }
  return args;
}

function normalizeText(value) {
  return String(value ?? "").replace(/\u00a0/gu, " ").replace(/\s+/gu, " ").trim();
}

function wordCount(sentence) {
  return (normalizeText(sentence).match(/[A-Za-z0-9]+(?:'[A-Za-z0-9]+)?/gu) ?? []).length;
}

async function readJson(filePath) {
  return JSON.parse(await readFile(filePath, "utf8"));
}

async function readJsonl(filePath) {
  const text = await readFile(filePath, "utf8");
  return text
    .split(/\r?\n/u)
    .filter((line) => line.trim())
    .map((line, index) => {
      try {
        return JSON.parse(line);
      } catch (error) {
        throw new Error(`Invalid JSONL at ${filePath}:${index + 1}: ${error.message}`);
      }
    });
}

